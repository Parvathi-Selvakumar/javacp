 public class SumElements {
    public static void main(String[] args){
        int[] arr = {10,20,30,40,50};
        int sum =0 ;
        for(int i=0; i<arr.length; i++){
            sum = sum + arr[i];//for product assign the value sum =1
        }
        System.out.println("Sum =" + sum);
    }
}
 
    

